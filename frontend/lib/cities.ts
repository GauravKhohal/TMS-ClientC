export function searchCities(query: string): CityHit[] {
  if (!query || query.trim().length < 2) return [];
  const q = query.trim().toLowerCase();
  return CITIES
    .filter(c => {
      const nameMatch  = c.name.toLowerCase().includes(q);
      const aliasMatch = c.aliases?.some(a => a.toLowerCase().includes(q)) ?? false;
      const stateMatch = c.state.toLowerCase().includes(q);
      return nameMatch || aliasMatch || stateMatch;
    })
    .sort((a, b) => {
      // Exact-start matches first
      const aStart = a.name.toLowerCase().startsWith(q) || (a.aliases?.some(al => al.toLowerCase().startsWith(q)) ?? false);
      const bStart = b.name.toLowerCase().startsWith(q) || (b.aliases?.some(al => al.toLowerCase().startsWith(q)) ?? false);
      return Number(bStart) - Number(aStart);
    })
    .slice(0, 8)
    .map(({ name, state, lat, lng }) => ({ name, state, lat, lng }));
}
